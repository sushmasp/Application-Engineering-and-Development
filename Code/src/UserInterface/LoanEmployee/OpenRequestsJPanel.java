    public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)tblLoan.getModel();
        dtm.setRowCount(0);
   
               if(!department.getWorkQueue().getWorkRequestList().isEmpty())
               {
                    for (WorkRequest w : department.getWorkQueue().getWorkRequestList())
                    {
                        if(w.getStatus().equals("Open") && (w.getReceiver()==null))
                        {
                            if(w instanceof LoanWorkRequest)
                            {
                                if(((LoanWorkRequest) w).getEmployee()==null)
                                {
                                    Object[] row = new Object[5];
                                    row[0]= w;
                                    row[1]= ((LoanWorkRequest) w).getCustomer();
                                    row[2]= ((LoanWorkRequest) w).getCustomeRrequestedLoanAmount();
                                    row[3]= ((LoanWorkRequest) w).getPurpose();
                                    row[4]= w.getStatus();
                                    dtm.addRow(row);
                                }
                            }
                        }
                       
                    }
               }
               
        
        
    }
    
   
    
