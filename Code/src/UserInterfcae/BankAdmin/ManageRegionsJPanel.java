    private void btnRemoveRegionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveRegionActionPerformed
      int rowSelected = tblRegions.getSelectedRow();
      
      if (rowSelected < 0)
      {
          JOptionPane.showMessageDialog(null, "Please select a row.","WARNING",JOptionPane.WARNING_MESSAGE);
          return;
      }
      
      Region region = (Region)tblRegions.getValueAt(rowSelected, 0);
      business.getRegionDirectory().removeRegion(region);
      
      JOptionPane.showMessageDialog(null, "Region is deleted successfully.","INFORMATION",JOptionPane.INFORMATION_MESSAGE);
     
      populateTable();
      
    }//GEN-LAST:event_btnRemoveRegionActionPerformed


