    private void btnUnlockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnlockActionPerformed
        String password = String.valueOf(txtPassword.getPassword());
        if (txtSSN.getText().trim().equals(""))
        {
           JOptionPane.showMessageDialog(null,"SSN is mandatory.","WARNING",JOptionPane.WARNING_MESSAGE);
           return;
        }
        if (password.equals(""))
        {
           JOptionPane.showMessageDialog(null,"Password is mandatory.","WARNING",JOptionPane.WARNING_MESSAGE);
           return;
        }
        String PasswordPattern="((?=.*\\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%]).{6,10})";
        Pattern pattern = Pattern.compile(PasswordPattern);
        Matcher matcher = pattern.matcher(password);
	if(matcher.matches()==false)
        {
          JOptionPane.showMessageDialog(null,"Please enter a Strong Password(1 numeral,1 lowerCase,1 UpperCase, 1 SpecialCharacter([@#$%]).","WARNING",JOptionPane.WARNING_MESSAGE);
          return;  
        }
       
        if(userAccount.getPerson().getSSN().equals(txtSSN.getText().trim()))
        {
           userAccount.setPassword(password);
           userAccount.setIsLocked(Boolean.FALSE);
        }
        else
        { JOptionPane.showMessageDialog(null,"SSN does not match with records.","WARNING",JOptionPane.WARNING_MESSAGE);
          return;  
        }
        
        JOptionPane.showMessageDialog(null, "UserAccount is successfully unlocked.Login after sometime.","INFORMATION",JOptionPane.INFORMATION_MESSAGE);
         
        userProcessContainer.removeAll();
        JPanel blankJPanel = new JPanel();
        userProcessContainer.add("blank", blankJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_btnUnlockActionPerformed


