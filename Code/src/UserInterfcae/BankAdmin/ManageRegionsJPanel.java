    private void btnUpdateRegionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateRegionActionPerformed
      int rowSelected = tblRegions.getSelectedRow();
      
      if (rowSelected < 0)
      {
          JOptionPane.showMessageDialog(null, "Please select a region.","WARNING",JOptionPane.WARNING_MESSAGE);
          return;
      }
      
      Region region = (Region)tblRegions.getValueAt(rowSelected,0);
     
        Employee emp = (Employee)tblRegions.getValueAt(rowSelected, 1);
        UpdateRegionJPanel updateRegion = new UpdateRegionJPanel(userProcessContainer,region,business,emp);
        userProcessContainer.add("UpdateRegionJPanel",updateRegion);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);       
    }//GEN-LAST:event_btnUpdateRegionActionPerformed

