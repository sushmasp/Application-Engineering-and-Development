    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed

    DepartmentType type =(DepartmentType) comboDept.getSelectedItem();
    String deptName= type.getValue();
    
    for(Department dept: branch.getDepartmentDirectory().getDepartmentList())
    {
        if(dept.getDepartmentName().equals(deptName))
        {
            JOptionPane.showMessageDialog(null, deptName + " already exists in the branch.","WARNING",JOptionPane.WARNING_MESSAGE);
            return;
        }
    }
    
    Department department = branch.getDepartmentDirectory().createDepartment(type);
        
       	JOptionPane.showMessageDialog(null, deptName + " is created successfully.","INFORMATION",JOptionPane.INFORMATION_MESSAGE);     
    }//GEN-LAST:event_btnCreateActionPerformed

