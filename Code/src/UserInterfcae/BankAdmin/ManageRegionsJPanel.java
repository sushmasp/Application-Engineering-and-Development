    public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel) tblRegions.getModel();
        dtm.setRowCount(0);
        
        if(!(business.getRegionDirectory().getRegionList().isEmpty()))
        {
            for (Region region : business.getRegionDirectory().getRegionList())
            {
                Object[] row = new Object[2];
                row[0]= region;
                
                ArrayList<Employee> employees = new ArrayList<Employee>();
                employees = region.getEmployeeDirectory().getEmployeeList();
                if(employees.isEmpty())
                {
                    row[1]="";
                }
                else
                {
                    for(Employee e: employees)
                    {
                        row[1] = e;
                    }
                }
                dtm.addRow(row);
            }
        }
    }
    
