    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        String name = txtBranchName.getText().trim();
        if(name.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Branch Name is mandatory.","WARNING",JOptionPane.WARNING_MESSAGE);
            return;
        }

        JOptionPane.showMessageDialog(null, "Directed towards assigning a Branch Manager to the newly created branch.","INFORMATION",JOptionPane.INFORMATION_MESSAGE);

        txtBranchName.setText("");

        NewBranchManagerJPanel newBranchManager = new NewBranchManagerJPanel(userProcessContainer,region,name,business);
        userProcessContainer.add("NewBranchManagerJPanel",newBranchManager);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageBranchesJPanel manageBranches = (ManageBranchesJPanel) component;
        manageBranches.populateTable();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed


