    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed

        int rowSelected = tblLoan.getSelectedRow();

        if (rowSelected < 0)
        {
            JOptionPane.showMessageDialog(null, "Please select a row.","WARNING",JOptionPane.WARNING_MESSAGE);
            return;
        }

        WorkRequest w = (WorkRequest) tblLoan.getValueAt(rowSelected, 0);
        LoanWorkRequest loanWorkrequest=null;
        Employee emp=null;
        Customer cus = null;
         // Setting the employee in the work request of employee
        if(w instanceof LoanWorkRequest)
        {
             emp = (Employee)userAccount.getPerson();
            ((LoanWorkRequest) w).setEmployee(emp);
             w.setReceiver(userAccount);
             userAccount.getWorkQueue().getWorkRequestList().add((LoanWorkRequest) w); 
            
             loanWorkrequest =(LoanWorkRequest) w;
        }
        
        // Setting the employee in the work request of customer requested
        cus = loanWorkrequest.getCustomer();
        for(UserAccount ua:branch.getUserAccountDirectory().getUserAccountList())
        {
            if(ua.getPerson()==cus)
            {
                for(WorkRequest wr : ua.getWorkQueue().getWorkRequestList())
                {
                    if(wr instanceof LoanWorkRequest)
                    {
                        if(wr==loanWorkrequest)
                        {
                            w.setReceiver(userAccount);
                           ((LoanWorkRequest) wr).setEmployee(emp) ;
                        }
                    }
                }
            }
        }
        
        JOptionPane.showMessageDialog(null, "Work Request is assigned to oneself successfully.","INFORMATION",JOptionPane.INFORMATION_MESSAGE);

        populateTable();

    }//GEN-LAST:event_btnAssignActionPerformed

